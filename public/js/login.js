function showup(){
  $(".sin").hide();
  $(".sup").show();
  $(".txt1").empty();
  $(".txt1").text("Already have an account?");
  $(".umma").hide();
  $(".umm").hide();
  $(".umm1").show();
}

function showin(){
  $(".sup").hide();
  $(".sin").show();
  $(".txt1").empty();
  $(".txt1").text("Create an account?");
  $(".umma").show();
  $(".umm").show();
  $(".umm1").hide();
}